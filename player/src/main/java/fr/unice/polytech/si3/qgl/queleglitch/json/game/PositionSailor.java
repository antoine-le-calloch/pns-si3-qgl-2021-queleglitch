package fr.unice.polytech.si3.qgl.queleglitch.json.game;

public class PositionSailor {
    public int x;
    public int y;

    public PositionSailor(int x, int y){
        this.x = x;
        this.y = y;
    }

    /**
     * @return <b>The position : x.</b>
     */
    public int getX() {
        return x;
    }

    /**
     * @return <b>The position : y.</b>
     */
    public int getY() {
        return y;
    }
}
