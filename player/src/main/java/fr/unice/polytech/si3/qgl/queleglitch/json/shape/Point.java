package fr.unice.polytech.si3.qgl.queleglitch.json.shape;

public class Point {

    private double x;
    private double y;

    /**
     * <p>Getter.</p>
     */
    public double getX(){
        return x;
    }

    public double getY(){
        return y;
    }

    /**
     * <p>Setter.</p>
     */
    public void setX(double x) { this.x = x; }

    public void setY(double y) { this.y = y; }
}
